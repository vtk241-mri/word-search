import fs from "node:fs/promises";
import path from "node:path";

const ROOT = process.cwd();
const LOCK_FILE = path.join(ROOT, "package-lock.json");
const OUTPUT_FILE = path.join(ROOT, "THIRD_PARTY_LICENSES.md");

function normalizeLicense(license) {
  if (!license) return "UNKNOWN";
  if (typeof license === "string") return license;
  if (Array.isArray(license)) {
    return license.map((item) => item.type || String(item)).join(", ");
  }
  if (typeof license === "object" && license.type) return license.type;
  return String(license);
}

async function readJson(filePath) {
  return JSON.parse(await fs.readFile(filePath, "utf8"));
}

async function resolvePackageInfo(packagePath, meta) {
  const manifestPath = path.join(ROOT, packagePath, "package.json");

  try {
    const manifest = await readJson(manifestPath);
    return {
      name: manifest.name || meta.name || packagePath,
      version: manifest.version || meta.version || "unknown",
      license: normalizeLicense(manifest.license || manifest.licenses),
      homepage:
        manifest.homepage ||
        (typeof manifest.repository === "string"
          ? manifest.repository
          : manifest.repository?.url) ||
        "",
    };
  } catch {
    return {
      name: meta.name || packagePath.replace(/^node_modules[\\/]/, ""),
      version: meta.version || "unknown",
      license: normalizeLicense(meta.license),
      homepage: "",
    };
  }
}

async function main() {
  const lock = await readJson(LOCK_FILE);
  const packageEntries = Object.entries(lock.packages || {}).filter(
    ([packagePath]) => packagePath
  );

  const resolved = await Promise.all(
    packageEntries.map(([packagePath, meta]) =>
      resolvePackageInfo(packagePath, meta)
    )
  );

  const unique = Array.from(
    new Map(
      resolved.map((entry) => [`${entry.name}@${entry.version}`, entry])
    ).values()
  ).sort((a, b) => a.name.localeCompare(b.name) || a.version.localeCompare(b.version));

  const counts = unique.reduce((acc, entry) => {
    acc[entry.license] = (acc[entry.license] || 0) + 1;
    return acc;
  }, {});

  const summaryLines = Object.entries(counts)
    .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))
    .map(([license, count]) => `- ${license}: ${count}`);

  const detailLines = unique.map((entry) => {
    const suffix = entry.homepage ? ` | ${entry.homepage}` : "";
    return `- ${entry.name} ${entry.version} | ${entry.license}${suffix}`;
  });

  const content = [
    "# Third-Party License Report",
    "",
    "This file is generated by `npm run licenses:report` using the installed `node_modules` tree and `package-lock.json` as a local license-checking source.",
    "",
    "- Project: Word Search",
    "- Started: 2025",
    `- Generated at: ${new Date().toISOString()}`,
    `- Packages scanned: ${unique.length}`,
    "",
    "## Summary by License",
    "",
    ...summaryLines,
    "",
    "## Detailed Package List",
    "",
    ...detailLines,
  ].join("\n");

  await fs.writeFile(OUTPUT_FILE, content, "utf8");
  console.log(`Generated license report: ${path.relative(ROOT, OUTPUT_FILE)}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
